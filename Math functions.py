import math
nums = [25, 36, 49, 64, 81, 100]

print(f"Absolute value of 99 is {abs(-99)}")
print(f"Maximum number from nums is {max(nums)}")
print(f"Minimum number from nums is {min(nums)}")
print(f"Average of nums is {sum(nums)/len(nums):.2f}")
print(f"Square roots of each number in nums {[math.sqrt(n) for n in nums]}")
print(f"Rounded number of 45.6789 to 2 decimal places is {round(45.6789, 2)}")
print(f"value of 2 power 8 is {pow(2,8)}")